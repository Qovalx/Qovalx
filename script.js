const roleMap = {
  en: { Broker: "Broker", Developer: "Developer", Investor: "Investor", "Strategic Partner": "Strategic Partner" },
  ar: { Broker: "وسيط", Developer: "مطور", Investor: "مستثمر", "Strategic Partner": "شريك استراتيجي" }
};

const body = document.body;
const langToggle = document.getElementById('langToggle');
const menuToggle = document.getElementById('menuToggle');
const mobileMenu = document.getElementById('mobileMenu');
const modal = document.getElementById('accessModal');
const accessForm = document.getElementById('accessForm');
const copyEmailBtn = document.getElementById('copyEmailBtn');
const feedback = document.getElementById('formFeedback');
const founderEmail = 'founder@qovalx.com';

function applyTranslations(lang) {
  const dict = translations[lang];
  body.setAttribute('data-lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  langToggle.textContent = lang === 'en' ? 'AR' : 'EN';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key]) el.placeholder = dict[key];
  });

  document.querySelectorAll('select option[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });

  localStorage.setItem('qovalx-lang', lang);
}

function openModal() {
  modal.classList.add('open');
  modal.setAttribute('aria-hidden', 'false');
  body.classList.add('modal-open');
}

function closeModal() {
  modal.classList.remove('open');
  modal.setAttribute('aria-hidden', 'true');
  body.classList.remove('modal-open');
}

function toggleMobileMenu(force) {
  const shouldOpen = typeof force === 'boolean' ? force : !mobileMenu.classList.contains('open');
  mobileMenu.classList.toggle('open', shouldOpen);
  mobileMenu.setAttribute('aria-hidden', shouldOpen ? 'false' : 'true');
}

function activateTab(role) {
  document.querySelectorAll('.tab-btn').forEach((btn) => {
    const active = btn.dataset.role === role;
    btn.classList.toggle('active', active);
    btn.setAttribute('aria-selected', active ? 'true' : 'false');
  });

  document.querySelectorAll('.audience-panel').forEach((panel) => {
    panel.classList.toggle('active', panel.dataset.panel === role);
  });
}

function buildMailto(role) {
  const lang = body.getAttribute('data-lang') || 'en';
  const name = accessForm.querySelector('input[name="name"]').value.trim();
  const email = accessForm.querySelector('input[name="email"]').value.trim();
  const location = accessForm.querySelector('input[name="location"]').value.trim();
  const message = accessForm.querySelector('textarea[name="message"]').value.trim();
  const displayRole = roleMap[lang][role] || role;

  const subject = lang === 'ar'
    ? `طلب وصول مبكر - ${displayRole} - QOVALX`
    : `QOVALX Early Access Request - ${displayRole}`;

  const lines = lang === 'ar'
    ? [
        'مرحباً فريق كوفالكس،',
        '',
        'أرغب في طلب الوصول المبكر.',
        '',
        `الاسم: ${name}`,
        `البريد الإلكتروني: ${email}`,
        `الصفة: ${displayRole}`,
        `الموقع: ${location}`,
        `الرسالة: ${message}`,
        '',
        'شكراً لكم.'
      ]
    : [
        'Hello QOVALX Team,',
        '',
        'I would like to request early access.',
        '',
        `Name: ${name}`,
        `Email: ${email}`,
        `Role: ${displayRole}`,
        `Location: ${location}`,
        `Message: ${message}`,
        '',
        'Thank you.'
      ];

  return `mailto:${founderEmail}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(lines.join('\n'))}`;
}

function setFeedback(message, type = 'success') {
  feedback.textContent = message;
  feedback.className = `form-feedback ${type}`;
}

function sendQuickRoleMail(role) {
  const formRole = accessForm.querySelector('select[name="role"]');
  formRole.value = role;
  closeModal();
  document.getElementById('contact').scrollIntoView({ behavior: 'smooth', block: 'start' });
  setTimeout(() => accessForm.email.focus(), 450);
}

langToggle.addEventListener('click', () => {
  const current = body.getAttribute('data-lang') || 'en';
  applyTranslations(current === 'en' ? 'ar' : 'en');
});

menuToggle.addEventListener('click', () => toggleMobileMenu());

document.querySelectorAll('#mobileMenu a').forEach((link) => {
  link.addEventListener('click', () => toggleMobileMenu(false));
});

document.querySelectorAll('.open-access').forEach((btn) => {
  btn.addEventListener('click', openModal);
});

document.querySelectorAll('[data-close-modal]').forEach((el) => {
  el.addEventListener('click', closeModal);
});

window.addEventListener('keydown', (e) => {
  if (e.key === 'Escape') {
    closeModal();
    toggleMobileMenu(false);
  }
});

document.getElementById('openFullForm').addEventListener('click', () => {
  closeModal();
  document.getElementById('contact').scrollIntoView({ behavior: 'smooth', block: 'start' });
});

document.querySelectorAll('.modal-role').forEach((btn) => {
  btn.addEventListener('click', () => sendQuickRoleMail(btn.dataset.roleMail));
});

document.querySelectorAll('.tab-btn').forEach((btn) => {
  btn.addEventListener('click', () => activateTab(btn.dataset.role));
});

accessForm.addEventListener('submit', (e) => {
  e.preventDefault();
  const role = accessForm.role.value;
  const mailto = buildMailto(role);
  window.location.href = mailto;
  const lang = body.getAttribute('data-lang') || 'en';
  setFeedback(
    lang === 'ar'
      ? 'تم تجهيز طلبك. إذا لم يفتح البريد تلقائياً، انسخ بريد المؤسس وتواصل معنا مباشرة.'
      : 'Your request is ready. If your email app did not open automatically, copy the founder email and contact us directly.',
    'success'
  );
});

copyEmailBtn.addEventListener('click', async () => {
  const lang = body.getAttribute('data-lang') || 'en';
  try {
    await navigator.clipboard.writeText(founderEmail);
    setFeedback(lang === 'ar' ? 'تم نسخ بريد المؤسس.' : 'Founder email copied.', 'success');
  } catch (error) {
    setFeedback(lang === 'ar' ? 'تعذر النسخ تلقائياً. البريد هو founder@qovalx.com' : 'Could not copy automatically. The email is founder@qovalx.com', 'error');
  }
});

const preferredLang = localStorage.getItem('qovalx-lang') || 'en';
applyTranslations(preferredLang);
activateTab('brokers');
